namespace tycho
{
namespace graphics
{

	adapter::adapter(const char *name, formats &fmts) 
	{
		m_name = name;
		m_formats.swap(fmts);
	}
	
	int adapter::get_num_formats() const
	{
		return static_cast<int>(m_formats.size());
	}
	
	const adapter::format& adapter::get_format(int i) const
	{
		return m_formats[i];
	}

} // end namespace
} // end namespace
